Cypress.Commands.add('selectDropdown', (selector, value) => {
    cy.get(selector).select(value)
})

Cypress.Commands.add('setDateRange', (startSelector, endSelector, startDate, endDate) => {
    cy.get(startSelector).type(startDate)
    cy.get(endSelector).type(endDate)
})